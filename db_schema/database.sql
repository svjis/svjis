SELECT a.ID, a.COMPANY_ID, a.FIRST_NAME, a.LAST_NAME, a.ENABLED
FROM "USER" a
left join USER_HAS_ROLE h on (h.USER_ID = a.ID) and (h.ROLE_ID = 6)
where a.COMPANY_ID = 1 and h.ROLE_ID is null
